def imprimir_jogos_por_nome(parte_nome):
    encontrados = []
    for jogo in jogos:
        if parte_nome.lower() in jogo['nome'].lower():
            encontrados.append(jogo)

    if encontrados:
        for jogo in encontrados:
            print(f"Nome: {jogo['nome'].title()}")
            print()
    else:
        print("Nenhum jogo encontrado.")
